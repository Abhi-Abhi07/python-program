# Arithmetic Operators (+, -, *, /, %, **, //)
a=10
b=5

print("a + b =",a+b)
print("a - b =",a-b)
print("a * b =",a*b)
print("a / b =",a/b)

print("a % b =",a%b) # a > b
print("a % 3 =",a%3) # a > 3
print("b % b =",b%b) # b = b
print("b % 6 =",b%6) # b < 6 so return b
print("b % a =",b%a) # b < a so return b

print("a^b=",a**b)
print("5^2=",5**2) # 5*5
print("5^3=",5**3) # 5*5*5

print("a // b =",a//b) # floor division ( sign // )
print("10 / 3 =",10/3) # Normal division
print("10 // 3 =",10//3)
print("12 / 3 =",12/3)
print("12 // 3 =",12//3)
